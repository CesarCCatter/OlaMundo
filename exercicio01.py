'''
Elabore um programa que pergunte ao aluno suas 3 notas escolares.
O programa deverá calcular a média das 3 notas inseridas e
exibir esta medida
'''

# Etapa 1: entrada de dados
nota1 = float(input("informe a  sua primeira nota "))
nota2 = float(input("informe a  sua segunda nota"))
nota3 = float(input("informe a  sua terceira nota"))

# Etapa 2: processamento de dados
media =  (nota1 + nota2 + nota3) /3

# Etapa 3: Saída de sados ()resposta na tela do usuário
print("sua média é: ", media)

